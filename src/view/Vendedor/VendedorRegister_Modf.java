    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
                int idUsuario = ((Usuario) cmbUsuario.getSelectedItem()).getIdUsuario();
                Date fechaInicio = (Date) spnInicio.getValue();
                Date fechaFin = (Date) spnFin.getValue();
                String razonSocial = txtRazon.getText();

                Vendedor nuevoVendedor = new Vendedor(idVendedor, idUsuario, fechaInicio, fechaFin, razonSocial);
                VendedorDAO vendedorDAO = new VendedorDAO();
                int errorCode;

                if (isModify) {
                    errorCode = vendedorDAO.modificarVendedor(nuevoVendedor);
                } else {
                    errorCode = vendedorDAO.insertarVendedor(nuevoVendedor);
                }

                if (errorCode == 0) {
                    JOptionPane.showMessageDialog(VendedorRegister_Modf.this,
                            isModify ? "Vendedor modificado exitosamente." : "Vendedor registrado exitosamente.",
                            "Éxito",
                            JOptionPane.INFORMATION_MESSAGE);
                    if (vendedorView != null) {
                        vendedorView.refreshVendedores();
                    }
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(VendedorRegister_Modf.this,
                            isModify ? "Error: Vendedor no encontrado." : "Error: No se pudo registrar el vendedor.",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }

    }//GEN-LAST:event_btnGuardarActionPerformed

