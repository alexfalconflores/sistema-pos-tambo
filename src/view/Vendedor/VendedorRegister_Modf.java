    private void loadVendedorData() {
        VendedorDAO vendedorDAO = new VendedorDAO();
        Vendedor vendedor = vendedorDAO.obtenerVendedorPorId(idVendedor);
        if (vendedor != null) {
            for (int i = 0; i < cmbUsuario.getItemCount(); i++) {
                Usuario user = cmbUsuario.getItemAt(i);
                if (user.getIdUsuario() == vendedor.getIdUsuario()) {
                    cmbUsuario.setSelectedIndex(i);
                    break;
                }
            }
            spnInicio.setValue(vendedor.getFechaInicio());
            spnFin.setValue(vendedor.getFechaFin());
            txtRazon.setText(vendedor.getRazonSocial());
        }
    }
    private void configureUsuarios() {
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        List<Usuario> usuarios = usuarioDAO.obtenerUsuarios();

        if ("Admin".equalsIgnoreCase(usuario.getRol())) {
            for (Usuario user : usuarios) {
                cmbUsuario.addItem(user);
            }
        } else {
            for (Usuario user : usuarios) {
                if (user.getIdUsuario() == usuario.getIdUsuario()) {
                    cmbUsuario.addItem(user);
                    break;
                }
            }
        }
    }
